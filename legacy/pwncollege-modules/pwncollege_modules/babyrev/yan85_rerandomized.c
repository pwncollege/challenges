unsigned char VALUES[8] = { 1, 2, 4, 8, 16, 32, 64, 128 };
void shuffle_values()
{
  for (int i = 0; i < 65535; i++)
  {
    int j = rand() % 8;
    int k = rand() % 8;
    WORD_TYPE t = VALUES[j];
    VALUES[j] = VALUES[k];
    VALUES[k] = t;
  }
}

